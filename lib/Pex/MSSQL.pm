package Pex::MSSQL;
use strict;
use Pex;

sub Ping
{
    my $host = shift || return;
    my $port = shift || 1434;
    my $r;
    my %x;
    
    my $s = Pex::Socket::Udp->new
    (
        'PeerAddr'  => $host, 
        'PeerPort'  => $port, 
    );
    return if $s->IsError;

    $s->Send("\x02");
    $r = $s->Recv(-1, 10);
    $s->Close();
    
    if (! $r)  { return }
    
    $r =~ s/.*(ServerName.*)/$1/;
    %x = split(/;/, $r);
    return %x;   
}


